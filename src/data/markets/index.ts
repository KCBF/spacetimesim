export function getCommodityById(id: string): Commodity | undefined {
  return commodities.find((c) => c.id === id);
}

export function getIncidentsByYear(year: number): HistoricIncident[] {
  return historicIncidents.filter((i) => i.year === year);
}

export function getIncidentsByCommodity(commodityId: string): HistoricIncident[] {
  return historicIncidents.filter((i) => i.affectedMarkets.includes(commodityId));
}

export function getIncidentsByCategory(category: HistoricIncident['category']): HistoricIncident[] {
  return historicIncidents.filter((i) => i.category === category);
}

export function getIncidentsByImpact(impact: HistoricIncident['impact']): HistoricIncident[] {
  return historicIncidents.filter((i) => i.impact === impact);
}

export function getPriceAtYear(commodityId: string, year: number): number | undefined {
  const commodity = getCommodityById(commodityId);
  if (!commodity) return undefined;
  const point = commodity.history.find((p) => p.year === year);
  return point?.price;
}

export function getPriceRange(
  commodityId: string,
  startYear: number,
  endYear: number
): PricePoint[] {
  const commodity = getCommodityById(commodityId);
  if (!commodity) return [];
  return commodity.history.filter((p) => p.year >= startYear && p.year <= endYear);
}
